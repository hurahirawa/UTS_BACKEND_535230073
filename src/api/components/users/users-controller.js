const usersService = require('./users-service');
const { errorResponder, errorTypes } = require('../../../core/errors');

/**
 * Handle get list of users request
 * @param {object} request - Express request object
 * @param {object} response - Express response object
 * @param {object} next - Express route middlewares
 * @returns {object} Response object or pass an error to the next route
 */
async function getUsers(request, response, next) {
  try {
    let { page_number, page_size, sort, search } = request.query;
    
    // Set default values if not provided
    page_number = page_number ? parseInt(page_number) : 1;
    page_size = page_size ? parseInt(page_size) : 10;

    // Handle sorting
    let sortOptions = {};
    if (sort) {
      const [field, order] = sort.split(':');
      sortOptions[field] = order === 'desc' ? -1 : 1;
    } else {
      // Default sorting by email ascending
      sortOptions['email'] = 1;
    }

    // Handle search
    let searchOptions = {};
    if (search) {
      const [field, key] = search.split(':');
      searchOptions[field] = { $regex: key, $options: 'i' }; // Case-insensitive search
    }

    const count = await usersService.countUsers(searchOptions); // Total count of users

    const total_pages = Math.ceil(count / page_size);
    const has_previous_page = page_number > 1;
    const has_next_page = page_number < total_pages;

    const users = await usersService.getUsers(page_number, page_size, sortOptions, searchOptions);
    
    const mapUsers = users.map(user => ({
      id: user.id.toString(),
      name: user.name,
      email: user.email
    }))

    const responseData = {
      page_number,
      page_size,
      count,
      total_pages,
      has_previous_page,
      has_next_page,
      data: mapUsers
    };

    return response.status(200).json(responseData);
  } catch (error) {
    return next(error);
  }
}

